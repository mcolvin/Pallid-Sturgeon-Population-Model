					factor(rkm2bend(RKM_H[,x]),	levels=c(1:inputs$n_bends)),
					sum)
				H[is.na(H)]<-0				
				return(N+H)})	


			## FERTILIZATION
			### HOW MANY FEMALES IN EACH BEND
			FEM_BND<- sapply(1:inputs$nreps,function(x){
				N<-tapply(Z_H[,x]*SEX_H[,x],
					factor(rkm2bend(RKM_N[,x]),	levels=c(1:inputs$n_bends)),
